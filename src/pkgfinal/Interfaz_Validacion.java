    /**
     * Cuadro de texto en donde se ingresa el pin del usuario
     * @param evt 
     */
    private void IngresarPinTransaccionKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_IngresarPinTransaccionKeyTyped
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || c == KeyEvent.VK_BACK_SPACE || c == KeyEvent.VK_DELETE)) {
            evt.consume();  // Rechaza la entrada que no sea un n√∫mero, retroceso o suprimir
        }
        
         if (IngresarPinTransaccion.getText().length() >= limiteDePin ) {
            evt.consume(); // Rechaza la entrada si se alcanza la longitud m√°xima
        }
    }//GEN-LAST:event_IngresarPinTransaccionKeyTyped
    /**
     * Boton de confirmacion de validacion de pin que llama a diferentes operadores segun sea la operacion dada
     * @param evt 
     */
    private void BotonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonConfirmarActionPerformed
         int pinTransferencia = cliente.getPinTransf();
        
        int pinIngresado = Integer.parseInt(IngresarPinTransaccion.getText().trim());
        
        /*
        El valor ingresado en el valor proviene indica que ventana es la que tiene que volver a abrir una vez realice las operaciones
        1- Deposito
        2- Transferencia
        3- Pago de un servicio de entidad
        4- Pago de una tarjeta de credito
        */
        if (pinTransferencia == pinIngresado ){
             
            switch (proviene){
                case 1:
                    Interfaz_Realizar_Deposito Realizar_Deposito = Interfaz_Realizar_Deposito.getInstance();
                    Realizar_Deposito.RealizarDeposito();
                    this.dispose();
                    break;
                case 2:
                    Interfaz_Transferencia Transferencia= Interfaz_Transferencia.getInstance();
                    Transferencia.Realizar_Transferencia();
                    this.dispose();
                    break;
                case 3:
                    Interfaz_Realizar_Pagos_Entidad Pago_Entidad = Interfaz_Realizar_Pagos_Entidad.getInstance();
                    Pago_Entidad.Realizar_Pago();
                    this.dispose();
                    break;
                case 4:
                    Interfaz_Realizar_Pagos_Tarjetas Pago_tarjeta = Interfaz_Realizar_Pagos_Tarjetas.getInstance();
                    Pago_tarjeta.Realizar_Pago();
                    this.dispose();
                    break;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pin de Transferencia incorrecto, int√©ntelo de nuevo", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonConfirmarActionPerformed

