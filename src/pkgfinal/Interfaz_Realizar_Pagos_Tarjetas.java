    private void BotonPagotarjetasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonPagotarjetasActionPerformed
        if(IngresarNroTarjeta.getText().isEmpty() || Ingresar_MontoPagar.getText().isEmpty()) {
            int camposVacios = 0;
            StringBuilder mensajeError = new StringBuilder("Error: Uno o m치s campos est치n vac칤os en los siguientes campos:\n");

            if (IngresarNroTarjeta.getText().isEmpty()) {
                camposVacios++;
                mensajeError.append("- Numero de Tarjeta\n");
            }
            if (Ingresar_MontoPagar.getText().isEmpty()) {
                camposVacios++;
                mensajeError.append("- Monto\n");
            }

            if (camposVacios > 0) {
                // Mostrar solo los campos que no est치n completos
                JOptionPane.showMessageDialog(this, mensajeError.toString(), "Error", JOptionPane.ERROR_MESSAGE);
                return; 
            }
        }

        double montoIngresado = 0.0; // Valor por defecto si el campo de monto est치 vac칤o
        try {
            montoIngresado = Double.parseDouble(Ingresar_MontoPagar.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Error: Ingrese un monto v치lido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (montoIngresado < montoMinimo) {
            JOptionPane.showMessageDialog(this, "Error: El monto ingresado es menor al requerido", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (IngresarNroTarjeta.getText().length() != limiteDeNumeros2) {
            JOptionPane.showMessageDialog(this, "Error: Ingrese un n칰mero de tarjeta v치lido con 16 d칤gitos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Interfaz_Validacion confirmar = new Interfaz_Validacion(4);
        confirmar.setVisible(true);
    }//GEN-LAST:event_BotonPagotarjetasActionPerformed
    /**
     * Metodo que realiza el pago de las tarjetas
     */
    public void Realizar_Pago(){
        
        Long monto = Long.parseLong(Ingresar_MontoPagar.getText());
        Long montoTotal = cliente.getFondos()-monto;

        String LineaSobre1=String.format("Fondos: %-20d", montoTotal);

        /*
        Disminuimos el fondo ya que estamos realizando un pago
        */
        try (RandomAccessFile file = new RandomAccessFile("src\\Documentos\\Usuarios\\Usuario"+cliente.getArchivo()+".txt" , "rw")) {
            long posicion=0;
            int aux=1,lineaSob=12;        //En los archivos txt en la linea 11 esta el dato a sobreescribir
            String linea;
            while ((linea = file.readLine()) != null) {
                if (aux==lineaSob) {
                    posicion=file.getFilePointer();
                    break;
                }
                aux++;
            }

            file.seek(posicion);
            file.writeBytes(LineaSobre1);
            file.writeBytes(System.lineSeparator());

        } catch (IOException e) {
            e.printStackTrace();
        }
        /*
        Disminuimos la deuda que posee esa persona en el servicio dado
        */

        long deuda=Long.parseLong(ValorMaximo.getText().trim());

        montoTotal= deuda-monto;
        String LineaSobre2=String.format("%-20d", montoTotal); ;

        try (RandomAccessFile file = new RandomAccessFile("src\\Documentos\\TarjetasRegistradas\\Tarjetas"+archivo+".txt" , "rw")) {
            long posicion=0;
            int aux=1,lineaSob=8+posicionTar;        //En los archivos txt en la linea 11 esta el dato a sobreescribir
            String linea;
            while ((linea = file.readLine()) != null) {
                if (aux==lineaSob) {
                    posicion=file.getFilePointer();
                    break;
                }
                aux++;
            }

            file.seek(posicion);
            file.writeBytes(LineaSobre2);
            file.writeBytes(System.lineSeparator());

        } catch (IOException e) {
            e.printStackTrace();
        }
        
        Agregar_Pago_Historial();
        JOptionPane.showMessageDialog(this, "Pago de tarjeta realizado con exito!!", "Pago Exitosa", JOptionPane.INFORMATION_MESSAGE);
        Interfaz_Pantalla_Principal principal = Interfaz_Pantalla_Principal.getInstance();
        principal.setVisible(true);
        Detalles_Operacion detalles = new Detalles_Operacion(4,monto,IngresarNroTarjeta.getText());
        detalles.setVisible(true);
        dispose();

    }
    
    private void Agregar_Pago_Historial(){
        String ubicacion_Archivos="src\\Documentos\\Historiales\\Historial";
        int iteracion=0;
        while (true){
            iteracion++;
            String Terminacion=iteracion+".txt";
            try(RandomAccessFile file = new RandomAccessFile(ubicacion_Archivos+Terminacion, "rw")){
                
                String linea=linea=file.readLine();
                
                Integer NroCuenta=Integer.parseInt(linea.substring("NroCuenta: ".length()).trim());

                if (NroCuenta.equals(cliente.getNroCuenta())){
                    
                    while ((linea=file.readLine())!=null);
                    LocalDate fechaHora = LocalDate.now();
                    String LineaSobre1=String.format("|%-10s|%-30s|%-20d|%-12s|\n", fechaHora.toString(),"Pago Tarjeta "+IngresarNroTarjeta.getText() ,Long.parseLong(Ingresar_MontoPagar.getText()),"Negativo");
                    file.writeBytes(LineaSobre1);
                    String LineaDos="|----------|------------------------------|--------------------|------------|\n";
                    file.writeBytes(LineaDos);
                    break;
                }
                
            } catch(Exception e){
                System.out.println("Error");
                break;
            }
        }
    }
    
